package review;

public class Student {
    private Integer sno;
    private String stringame;
    private String tel;
    private Integer age;

    public Integer getAge() {
        return age;
    }

    public void setAge(Integer age) {
        this.age = age;
    }

    public Integer getSno() {
        return sno;
    }

    public void setSno(Integer sno) {
        this.sno = sno;
    }

    public String getStringame() {
        return stringame;
    }

    public void setStringame(String stringame) {
        this.stringame = stringame;
    }

    public String getTel() {
        return tel;
    }

    public void setTel(String tel) {
        this.tel = tel;
    }
}
